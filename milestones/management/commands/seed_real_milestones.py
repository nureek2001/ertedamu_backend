class Command(BaseCommand):
    help = "Seed REAL milestones"

    def handle(self, *args, **kwargs):
        self.stdout.write("🚀 Создаем категории...")

        cats = {}
        for slug, name in CATEGORIES:
            obj, _ = MilestoneCategory.objects.get_or_create(slug=slug, defaults={"name": name})
            cats[slug] = obj

        self.stdout.write("📊 Создаем реальные milestones...")

        created = 0

        for age in ages():
            data = get_milestones_for_age(age)

            for slug, items in data.items():
                for i, text in enumerate(items):
                    obj, is_created = Milestone.objects.get_or_create(
                        title=text,
                        category=cats[slug],
                        min_age_months=age,
                        max_age_months=age,
                        defaults={
                            "description": f"{text} — ожидаемый навык для возраста {age} месяцев",
                            "is_active": True,
                        }
                    )
                    if is_created:
                        created += 1

        self.stdout.write(self.style.SUCCESS(f"✅ Создано {created} реальных milestones"))