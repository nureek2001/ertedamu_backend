class Command(BaseCommand):
    help = "Создаёт milestone categories и milestones для 1-36 мес, 4 года, 5 лет, 6 лет"

    def add_arguments(self, parser):
        parser.add_argument(
            "--reset",
            action="store_true",
            help="Удалить все milestones перед заполнением",
        )

    @transaction.atomic
    def handle(self, *args, **options):
        reset = options["reset"]

        self.stdout.write(self.style.NOTICE("Создание категорий..."))

        category_map = {}
        for item in CATEGORIES:
            category, created = MilestoneCategory.objects.update_or_create(
                slug=item["slug"],
                defaults={"name": item["name"]},
            )
            category_map[item["slug"]] = category

            if created:
                self.stdout.write(self.style.SUCCESS(f"Создана категория: {category.name} ({category.slug})"))
            else:
                self.stdout.write(self.style.WARNING(f"Обновлена категория: {category.name} ({category.slug})"))

        if reset:
            deleted_count, _ = Milestone.objects.all().delete()
            self.stdout.write(self.style.WARNING(f"Удалено milestones: {deleted_count}"))

        self.stdout.write(self.style.NOTICE("Заполнение milestones..."))

        created_count = 0
        updated_count = 0

        for age_months, categories in MILESTONE_DATA.items():
            for slug, payload in categories.items():
                title, description = payload
                category = category_map[slug]

                milestone, created = Milestone.objects.update_or_create(
                    title=title,
                    category=category,
                    min_age_months=age_months,
                    max_age_months=age_months,
                    defaults={
                        "description": description,
                        "is_active": True,
                    },
                )

                if created:
                    created_count += 1
                else:
                    updated_count += 1

        self.stdout.write("")
        self.stdout.write(
            self.style.SUCCESS(
                f"Готово. Создано milestones: {created_count}, обновлено: {updated_count}"
            )
        )
        self.stdout.write(
            self.style.SUCCESS(
                f"Всего возрастных точек: {len(MILESTONE_DATA)}. "
                f"Категорий на каждую точку: {len(CATEGORIES)}. "
                f"Ожидаемое общее число milestones: {len(MILESTONE_DATA) * len(CATEGORIES)}"
            )
        )